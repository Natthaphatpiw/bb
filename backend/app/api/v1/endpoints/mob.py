    """
    Get Market Opportunity Briefing for a specific symbol

    Currently supports:
    - CO: Crude Oil (WTI)
    """
    symbol_upper = symbol.upper()

    if symbol_upper == "CO":
        return {
            "success": True,
            "data": generate_mob_for_crude_oil()
        }
    else:
        raise HTTPException(
            status_code=404,
            detail=f"Market Opportunity Briefing not available for symbol: {symbol}"
        )


